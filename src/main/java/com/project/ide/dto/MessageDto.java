package com.project.ide.dto;

public class MessageDto {
	private String msg;
	private Boolean err;

	public String getMsg() {
		return msg;
	}

	public void setMsg(String msg) {
		this.msg = msg;
	}

	public Boolean getErr() {
		return err;
	}

	public void setErr(Boolean err) {
		this.err = err;
	}
	
}
